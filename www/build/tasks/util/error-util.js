/**
 * Gulp utils for error.
 */
const envUtil = require("./env-util");

function handleErrorEvent() {
    const transpileContinueOnError = envUtil.get("transpileContinueOnError");
    if (transpileContinueOnError) {
        // @ts-ignore
        // tslint:disable-next-line:no-invalid-this
        this.emit("end");
    }
}
exports.handleErrorEvent = handleErrorEvent;

function handleErrorCount(errorCount) {
    const transpileContinueOnError = envUtil.get("transpileContinueOnError");
    if (errorCount > 0 && !transpileContinueOnError) {
        process.exit(1);
    }
}
exports.handleErrorCount = handleErrorCount;
module.exports = {
    handleErrorCount,
    handleErrorEvent
};
