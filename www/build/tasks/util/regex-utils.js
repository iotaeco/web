const through = require("through2");

function toWebUrl(file) {
    return file.replace(/\\/g, "/");
}
exports.toWebUrl = toWebUrl;

function stripJsExtension(file) {
    return file.replace(/(\.js)$/, "");
}
exports.stripJsExtension = stripJsExtension;

function stripLeadingSlash(file) {
    return file.replace(/^\.\//, "");
}
exports.stripLeadingSlash = stripLeadingSlash;

function replaceLeadingSlash(file, replace) {
    return file.replace(/^\.\//, replace);
}
exports.replaceLeadingSlash = replaceLeadingSlash;

function multiReplace(replacements) {
    return through.obj((file, encode, callback) => {
        let contents = file.contents.toString();
        if (replacements) {
            Object.keys(replacements).forEach(replacement => {
                contents = contents.replace(new RegExp(replacement, "g"), replacements[replacement]);
            });
        }
        file.contents = Buffer.from(contents);
        callback(null, file);
    });
}
exports.multiReplace = multiReplace;
