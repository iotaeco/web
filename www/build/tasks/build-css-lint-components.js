/**
 * Gulp tasks for linting components with style lint.
 */
const gulp = require("gulp");
const styleLint = require("gulp-stylelint");
const path = require("path");
const asyncUtil = require("./util/async-util");
const display = require("./util/display");
const uc = require("./util/unite-config");
gulp.task("build-css-lint-components", async () => {
    display.info("Running", "StyleLint for Components");
    const uniteConfig = await uc.getUniteConfig();
    return asyncUtil.stream(gulp.src(path.join(uniteConfig.dirs.www.src, `**/*.${uniteConfig.styleExtension}`))
        .pipe(styleLint({
            syntax: uniteConfig.styleExtension === "css" ? undefined : uniteConfig.styleExtension,
            reporters: [{
                formatter: "string",
                console: true
            }]
        }))
        .on("error", (err) => {
            display.error("StyleLint failed", err);
            process.exit(1);
        }));
});
